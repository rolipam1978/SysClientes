    private void mostrarClientes(List<ClientesBean> clientes) {
        tbClientes.getColumnModel().getColumn(0).setPreferredWidth(90); // Nome
        tbClientes.getColumnModel().getColumn(1).setPreferredWidth(30); // Telefone
        tbClientes.getColumnModel().getColumn(2).setPreferredWidth(30); // Celular
        tbClientes.getColumnModel().getColumn(3).setPreferredWidth(50); // Email

        while (tmClientes.getRowCount() > 0) {
            tmClientes.removeRow(0);
        }

        if (clientes.size() == 0) {
            //JOptionPane.showMessageDialog(this, "Nenhum Paciente Encontrado!");
        } else {
        }
        String[] campos = new String[]{null, null, null, null};
        for (int i = 0; i < clientes.size(); i++) {

            tmClientes.addRow(campos);
            tmClientes.setValueAt(clientes.get(i).getNome(), i, 0);
            tmClientes.setValueAt(clientes.get(i).getTelefone(), i, 1);
            tmClientes.setValueAt(clientes.get(i).getCelular(), i, 2);
            tmClientes.setValueAt(clientes.get(i).getEmail(), i, 3);
        }
    }    
    
    private void buscaLicenca() {
        ClientesControl cc = new ClientesControl();
        licencas = cc.listarLicencas(Integer.parseInt(tfIDCliente.getText()));
        mostrarLicencas(licencas);
    }    
    
    private void mostrarLicencas(List<ClientesBean> licencas) {
        tbLicencas.getColumnModel().getColumn(0).setPreferredWidth(20); // Data
        tbLicencas.getColumnModel().getColumn(1).setPreferredWidth(20); // Tipo Licença
        tbLicencas.getColumnModel().getColumn(2).setPreferredWidth(50); // Cod. Ativação
        tbLicencas.getColumnModel().getColumn(3).setPreferredWidth(10); // Flg_Reativação

        while (tmLicencas.getRowCount() > 0) {
            tmLicencas.removeRow(0);
        }

        if (clientes.size() == 0) {
            //JOptionPane.showMessageDialog(this, "Nenhum Paciente Encontrado!");
        } else {
        }
        String[] campos = new String[]{null, null, null, null};
        for (int i = 0; i < licencas.size(); i++) {

            tmLicencas.addRow(campos);
            if(licencas.get(i).getData_ativacao() == null){
                tmLicencas.setValueAt("", i, 0);
            }else{
                tmLicencas.setValueAt(sdf.format(licencas.get(i).getData_ativacao()), i, 0);
            }            
            tmLicencas.setValueAt(licencas.get(i).getTipo_licenca(), i, 1);
            tmLicencas.setValueAt(licencas.get(i).getCod_ativacao(), i, 2);
            tmLicencas.setValueAt(licencas.get(i).getReativacao(), i, 3);
        }
    }    
    
