    private void tbClientesLinhaSelecionada(JTable tb){
        if(tb.getSelectedRow() != -1){
            // Mostra Dados nos Campos
            tfIDCliente.setText(""+clientes.get(tb.getSelectedRow()).getId_cliente());
            tfDataCadastro.setText(sdf.format(clientes.get(tb.getSelectedRow()).getData_cadastro()));
            tfNome.setText(clientes.get(tb.getSelectedRow()).getNome());
            if(clientes.get(tb.getSelectedRow()).getSexo().equals("M")){
                rbM.setSelected(true);
            }else if(clientes.get(tb.getSelectedRow()).getSexo().equals("F")){
                rbF.setSelected(true);
            }
            tfEmail.setText(clientes.get(tb.getSelectedRow()).getEmail());
            tfTelefone.setText(clientes.get(tb.getSelectedRow()).getTelefone());
            tfCelular.setText(clientes.get(tb.getSelectedRow()).getCelular());
            tfEstado.setText(clientes.get(tb.getSelectedRow()).getEstado());
            tfCidade.setText(clientes.get(tb.getSelectedRow()).getCidade());
            tfCEP.setText(clientes.get(tb.getSelectedRow()).getCep());
            tfEmpresa.setText(clientes.get(tb.getSelectedRow()).getEmpresa());
            tfFax.setText(clientes.get(tb.getSelectedRow()).getFax());
            // Mostra Histórico de Licenças
            buscaLicenca();
        }else{
            tbClientes.getSelectionModel().clearSelection();
            limparCampos();
        }
    }
    
    private void tbLicencasLinhaSelecionada(JTable tb){
        if(tb.getSelectedRow() != -1){
            tfIDLicencas.setText(""+licencas.get(tb.getSelectedRow()).getId_licenca());
            tfSoftware.setText(licencas.get(tb.getSelectedRow()).getSoftware());
            if(licencas.get(tb.getSelectedRow()).getTipo_licenca().equals("TRIAL")){
                rbTrial.setSelected(true);
            }else if (licencas.get(tb.getSelectedRow()).getTipo_licenca().equals("FULL")){
                rbFull.setSelected(true);
            }
            tfDataAtivacao.setText(sdf.format(licencas.get(tb.getSelectedRow()).getData_ativacao()));
            tfCodAtivacao.setText(licencas.get(tb.getSelectedRow()).getCod_ativacao());
            if(licencas.get(tb.getSelectedRow()).getReativacao().equals("N")){
                rbNao.setSelected(true);
            }else if(licencas.get(tb.getSelectedRow()).getReativacao().equals("S")){
                rbSim.setSelected(true);
            }
            tfMotivoReativacao.setText(licencas.get(tb.getSelectedRow()).getMotivo_reativacao());
        }else{
            tbLicencas.getSelectionModel().clearSelection();
            limparCamposLicencas();
        }        
    }
    
