    private void geraIDNovoCadastro(){
        try {
            con = new ConexaoBD();
            con.conecta();
            String sql = "select max(id) as id from clientes";
            con.executeSQL(sql);
            if (con.rs.first()) {
                id = con.rs.getInt("id");

            } else {
                id = 0;

            }
            tfIDCliente.setText("" + (id + 1));
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao Gerar ID!");
        } finally {
            con.desconecta();
        }        
    }
    
